package game.dto;

public class Woodcutter {

	private int hp = 100; // 체력
	private int Strike = 5;
	//---------------------------------------------- ★ 나무꾼 정보
	
	public int getHp() {
		return hp;
	}
	
	public void setHp(int hp) {
		this.hp = hp;
	}
	public int getStrike() {
		return Strike;
	}
	public void setStrike(int striking) {
		Strike = striking;
	}
	//---------------------------------------------- ★ getter/setter 메서드
}

