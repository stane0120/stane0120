package practice;

public class practice11 {
	public static void main(String[] args) {
		int num = 10;
		
		System.out.println(+num); // 값에 +가 붙어서 출력
		System.out.println(-num); // 값에 -가 붙어서 출력
		System.out.println(num); // 값을 그대로 출력
		
		num = -num; // 값을 음수로 바꿔서 출력
		System.out.println(num); 
	}

}
